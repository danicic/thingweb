package de.webthing.desc;

import java.util.ArrayList;
import java.util.List;

public class ThingDescriptionBuilder {
    private String name;
    private String[] protocols;
    private String[] encodings;
    private List<ThingDescription.ActionDescription> actions = new ArrayList<>();
    private List<ThingDescription.PropertyDescription> properties = new ArrayList<>();

    public ThingDescriptionBuilder setName(String name) {
        this.name = name;
        return this;
    }

    public ThingDescriptionBuilder setProtocols(String[] protocols) {
        this.protocols = protocols;
        return this;
    }

    public ThingDescriptionBuilder setEncodings(String[] encodings) {
        this.encodings = encodings;
        return this;
    }

    public ThingDescriptionBuilder addAction(ThingDescription.ActionDescription action) {
        this.actions.add(action);
        return this;
    }

    public ThingDescriptionBuilder addProperty(ThingDescription.PropertyDescription property) {
        this.properties.add(property);
        return this;
    }

    public ThingDescription build() {
        //Java is ugly sometimes...
        return new ThingDescription(name, protocols, encodings,
                actions.toArray(new ThingDescription.ActionDescription[actions.size()]),
                properties.toArray(new ThingDescription.PropertyDescription[properties.size()]));
    }
}